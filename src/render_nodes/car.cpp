void Car::draw(double dt) {
    glBindVertexArray(VAO);

    shader.setVec3f("baseColor", glm::vec3(1, 0.376, 0));
    shader.setMat4("model", glm::translate(glm::mat4(1.0f), position));
    glDrawArrays(GL_TRIANGLES, 0, 36);
}
